function renderPersona (vp) {
    var tg = "#" + vp;
    var contPeople = $(tg);
    if (personas[vp]) {
        var leng = personas[vp].length;
        var etiquetas = '';
        for (var i = 0; i < leng; i++) {    
            etiquetas += '<div class="cont-movilizador">' +
                '<div class="title-movilizador">' +
                '<h1>Tipo de Movilizador</h1></div>' +
                '<div class="cont-ilustracion">' +
                ' <img src="../img/ilustraciones-movilizadores/' + personas[vp][i].img + '" ><p class="cont-name">' + personas[vp][i].name + '</p></div>' +
                '<div class="cont-info-movilizador-ppal"><p>Número de personas impactadas</p><span class="total"> ' + personas[vp][i].impacts.total + '</span>' +
                '<div class="cont-hr"><hr class="right-decoration"><hr class="divider"></div>' +
                '<p>Pertenece a: </p><h2>' + personas[vp][i].direccion + '</h2>' +
                '<div class="cont-hr"><hr class="left-decoration"><hr class="divider"></div><p>' + personas[vp][i].descripton + '</p></div></div>';
        }
        contPeople.empty();
        contPeople.append(etiquetas);
    }
}

